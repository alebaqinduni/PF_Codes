#include <iostream>
using namespace std;
int main()
{
    // int arr[2][5] = {{1,2,3,4,5},{5,6,7,8,9}};

    cout << ceil(5.2) << endl;  // 6-round up
    cout << floor(5.2) << endl; // 5-round down
    cout << round(5.2) << endl; // 5-round to nearest
    cout << sin(90) << endl;    // 1
    cout << cos(90) << endl;    // 0
    cout << tan(90) << endl;    // undefined

    return 0;
}